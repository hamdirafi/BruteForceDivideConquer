public class NilaiMahasiswa {
    String nama;
    int nim, tahunMasuk, uts, uas;

    public NilaiMahasiswa(String nama, int nim, int tahunMasuk, int uts, int uas) {
        this.nama = nama;
        this.nim = nim;
        this.tahunMasuk = tahunMasuk;
        this.uts = uts;
        this.uas = uas;
    }
}
